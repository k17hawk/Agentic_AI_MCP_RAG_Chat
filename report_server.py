#!/usr/bin/env python3
"""
FastAPI server to serve performance reports.
Run with: uvicorn report_server:app --reload --host 0.0.0.0 --port 8000
"""

import os
from pathlib import Path
from datetime import datetime
from fastapi import FastAPI, HTTPException
from fastapi.responses import HTMLResponse, FileResponse
from fastapi.staticfiles import StaticFiles
import aiofiles

app = FastAPI(title="Trading Report Server", description="Serves generated performance reports")

# Configuration
REPORTS_BASE = Path("discovery_outputs")
STATIC_DIR = Path("static_reports")  # For any custom static files

# Ensure static directory exists
STATIC_DIR.mkdir(exist_ok=True)

def find_all_report_files() -> list:
    """Recursively find all HTML report files in discovery_outputs."""
    report_files = []
    if not REPORTS_BASE.exists():
        return report_files
    
    for html_file in REPORTS_BASE.rglob("*.html"):
        # Exclude template files (if any)
        if "template" in html_file.name.lower():
            continue
        rel_path = html_file.relative_to(REPORTS_BASE)
        report_files.append({
            "path": str(rel_path),
            "name": html_file.name,
            "run_dir": html_file.parent.name,
            "modified": datetime.fromtimestamp(html_file.stat().st_mtime).strftime("%Y-%m-%d %H:%M:%S"),
            "size_kb": round(html_file.stat().st_size / 1024, 1)
        })
    # Sort by modification time, newest first
    report_files.sort(key=lambda x: x["modified"], reverse=True)
    return report_files

