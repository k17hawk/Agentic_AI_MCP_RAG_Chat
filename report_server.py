@app.get("/reports/{path:path}")
async def serve_report(path: str):
    """Serve a specific HTML report file."""
    file_path = REPORTS_BASE / path
    # Security: ensure path is within REPORTS_BASE
    try:
        file_path = file_path.resolve()
        REPORTS_BASE.resolve().parent  # just to get base
        if not str(file_path).startswith(str(REPORTS_BASE.resolve())):
            raise HTTPException(status_code=403, detail="Forbidden")
    except Exception:
        raise HTTPException(status_code=403, detail="Invalid path")
    
    if not file_path.exists() or not file_path.is_file():
        raise HTTPException(status_code=404, detail="Report not found")
    
    # Only serve HTML files (for security)
    if file_path.suffix.lower() != '.html':
        raise HTTPException(status_code=403, detail="Only HTML files are served")
    
    return FileResponse(file_path, media_type="text/html")

@app.get("/latest", response_class=HTMLResponse)
async def latest_report():
    """Redirect to the most recent report."""
    reports = find_all_report_files()
    if not reports:
        return HTMLResponse(content="<h2>No reports found</h2>", status_code=404)
    latest = reports[0]['path']
    return HTMLResponse(content=f"""
    <html><head><meta http-equiv="refresh" content="0; url=/reports/{latest}"></head>
    <body>Redirecting to <a href="/reports/{latest}">latest report</a>...</body>
    </html>
    """)

@app.get("/health")
async def health():
    return {"status": "ok", "reports_found": len(find_all_report_files())}

